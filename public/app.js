// custom gender tripper
$(document).ready(function() {
  $('#custom').click(function() {
    $('.custom-genders').show();
  });
});

$(document).ready(function() {
  $('#male').click(function() {
    $('.custom-genders').hide();
  })
});

$(document).ready(function() {
  $('#female').click(function() {
    $('.custom-genders').hide();
  })
}); 




